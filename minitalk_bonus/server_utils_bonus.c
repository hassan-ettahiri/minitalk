void	print_array(char *t, int *i, int *bit)
{
	ft_printf("%s", *t);
	*i = 0;
	*bit = 0;
}

void	store_in_array(char *t, int *i, int *j, int *bit)
{
	t[*j] = (char)*i;
	t[*j + 1] = '\0';
	(*j)++;
	*i = 0;
	*bit = 0;
}

void	meme(char *t, int *c, int *bit, int *j)
{
	int	i;

	i = 0;
	while (i < 7000000)
	{
		t[i] = 0;
		i++;
	}
	*c = 0;
	*bit = 0;
	*j = 0;
}
